Ext.ns("Ynzc");
Ext.ns("Ynzc.manage");
Ynzc.manage.SendInfoUpdate=Ext.extend(Ext.Window,{
	id:'infoSendLook',
	initComponent:function(){
		Ext.apply(this,{
			title:'信息修改',
			id:'infoSendLook',
			width:650,
			modal:true,
			height:450,
			layout:'form',
			labelWidth:60,
			bodyStyle:'padding: 10px,10px,5px,10px',
			items:[{
				xtype:'combo',
				id:'infoTypeLook',
				fieldLabel:'信息类别',
				emptyText:'请选择信息类别',
				displayField:'types',
	 			editable:false,
				mode:'local',
				triggerAction:'all',
				store:new Ext.data.SimpleStore({
					fields:['types'],
					data:[['在线交流'],['最新公告']],
					autoload:true
				})
			},{
				xtype:'textfield',
				id:'infoTitleLook',
				fieldLabel:'信息标题',
				emptyText:'请输入信息标题'
			},{
				xtype:'htmleditor',
				fieldLabel:'信息内容',
				id:'infoContentLook'
			}],
			buttons:[{
				text:'保存',handler:function(){
					if(Ext.getCmp("infoTypeLook").getValue()==""){Ext.ux.Toast.msg("提示","请选择信息类型！");return}
					if(Ext.getCmp("infoTitleLook").getValue()==""){Ext.ux.Toast.msg("提示","请填写信息标题！");return}
					if(Ext.getCmp("infoContentLook").getValue()==""){Ext.ux.Toast.msg("提示","消息正文不能为空哦！");return}
					Ext.Ajax.request({
						url:'main/sendInfo.html?action=updateSendInfo',
						success:function(){
							Ext.ux.Toast.msg("提示","消息修改成功！");
							Ext.getCmp("sendMgr").getStore().reload();
							Ext.getCmp("infoSendLook").close();
						},
						params:{
							id:Ynzc.manage.SendInfoGridId,
							type:Ext.getCmp("infoTypeLook").getValue(),
							title:Ext.getCmp("infoTitleLook").getValue(),
							content:Ext.getCmp("infoContentLook").getValue()
						}
					});
				}
			},{
				text:'取消',
				handler:function(){
					Ext.getCmp("infoSendLook").close();
				}
			}]
		});
		Ynzc.manage.SendInfoUpdate.superclass.initComponent.apply(this,arguments);
	}
});